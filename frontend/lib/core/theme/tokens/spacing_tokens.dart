/// Spacing tokens define the app's spacing rhythm. Widgets should use these
/// instead of arbitrary padding/margin numbers, so spacing stays visually
/// consistent as new features are added in later milestones.
class SpacingTokens {
  const SpacingTokens._();

  static const double xs = 4;
  static const double sm = 8;
  static const double md = 16;
  static const double lg = 24;
  static const double xl = 32;
  static const double xxl = 48;
}
